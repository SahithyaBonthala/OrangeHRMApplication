package com.Loops;

public class ForExample {
	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++)
		{
			System.out.println(" Sahithya");
		}
		
		for(int i=-1; i>=-5; i--)
		{
			System.out.println(" SANNYU");
		}
		
		for(int i=-5; i<=-1; i++)
		{
			System.out.println(" Sahithya");
		}
		
		for(int i=5; i>=1; i--)
		{
			System.out.println(" SANNYU");
		}
		
		System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$");
		System.out.println();
		
		for(int var=10; var>=1; var=var-2)
		{
			System.out.println(var + "-" + "Selenium");
			System.out.println(var);
		}
		
		System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$");
		System.out.println();
		
		for(int var1=1; var1<=10; var1=var1+3)
		{
			System.out.println(var1 + "-" + "LiveTech");
		}
		
	}

}
